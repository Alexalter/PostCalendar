im Kalender ist vom Benutzer $pc_author ein Termin für ein Meeting angelegt worden. 

Termin       : $event_subject
Beschreibung : $pc_description
Datum        : $startDate
Uhrzeit      : $pc_start_time
Dauer        : $pc_dur_hours:$pc_dur_minutes

Der Termin mit weiteren Details kann unter 
$pc_URL
eingesehen werden. 

